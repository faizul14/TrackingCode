function isSortedAndHow(array) {
    const result = ['yes, ascending', 'yes, descending']
    const originalArr = array.join('')
    const asc = originalArr === [...array].sort((a, b) => a - b).join('')
    const desc = originalArr === [...array].sort((a, b) => b - a).join('')
    return asc ? result[0] : desc ? result[1] : 'no'
}
